/**
 * Task 1 : push method add new element at last position
 */
const arr = [1, 56, 89, 1, 4];

arr.push(57);

console.log(arr);

/**
 * Task 2 : pop method remove the last element
 */

arr.pop();
console.log(arr);

/**
 * Task 3 : shift method remove first element
 */

arr.shift();
console.log(arr);

/**
 * Task 4 : unshift method add new element at beginning of the array
 */

arr.unshift(5);
console.log(arr);
